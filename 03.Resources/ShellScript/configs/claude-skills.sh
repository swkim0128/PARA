#!/bin/bash

# ---------------------------------------------------------
# [1] 클로드 코드가 실행 중인 패널 자동 찾기
# ---------------------------------------------------------
TARGET_PANE=""

# 현재 윈도우의 모든 패널 ID와 TTY(터미널 포트)를 가져와서 반복 검사
for info in $(tmux list-panes -F "#{pane_id},#{pane_tty}"); do
    PANE_ID="${info%,*}"
    PANE_TTY="${info#*,}"

    # 해당 터미널(TTY)에서 실행 중인 프로세스 목록 중 'claude'가 있는지 확인
    if ps -t "$PANE_TTY" | grep -iq "[c]laude"; then
        TARGET_PANE="$PANE_ID"
        break
    fi
done

# 만약 클로드가 실행 중인 패널을 찾지 못했다면 경고 메시지 띄우고 종료
if [ -z "$TARGET_PANE" ]; then
    tmux display-message "❌ 클로드 코드가 실행 중인 패널을 찾을 수 없습니다!"
    exit 1
fi

# ---------------------------------------------------------
# [2] 클로드 코드 스킬 목록
# ---------------------------------------------------------
SKILLS="
🧪 [테스트] 인사말 출력 | 안녕! Tmux와 클로드 코드 연동 테스트 중이야. 짧고 유쾌하게 인사 한마디 부탁해!
🧪 [테스트] 현재 위치 확인 | 현재 디렉토리에 어떤 파일들이 있는지 확인(ls)하고, 파일 목록만 간략하게 알려줘.
🧪 [테스트] 개발자 유머 | 방금 설정이 너무 힘들었어. 기분 전환하게 개발자 관련 짧은 농담이나 유머 하나만 들려줘.
🔍 프로젝트 요약 | 현재 프로젝트의 전체 구조와 핵심 로직을 분석해서 요약해 줘.
🐛 코드 리뷰 | 방금 작성한 코드에서 개선할 점이나 잠재적인 버그가 있는지 확인해 줘.
📝 리팩토링 | 이 코드를 더 읽기 쉽고 효율적으로 리팩토링해 주고 이유를 설명해 줘.
✨ PHP 주석 달기 | 이 파일의 모든 함수에 대해 PHPDoc 표준에 맞춰 상세한 주석을 작성해 줘.
🚀 터미널 명령어 | 이 작업을 수행하기 위한 터미널 명령어를 작성하고 실행해 줘.
"

# ---------------------------------------------------------
# [3] fzf 팝업 메뉴 및 실행 로직
# ---------------------------------------------------------
SELECTED=$(echo "$SKILLS" | grep -v '^\s*$' | fzf \
  --prompt="🤖 클로드 스킬 선택 > " \
  --height=100% --layout=reverse --border=rounded)

if [ -z "$SELECTED" ]; then
    exit 0
fi

PROMPT=$(echo "$SELECTED" | awk -F '|' '{print $2}' | sed -e 's/^[[:space:]]*//' -e 's/[[:space:]]*$//')

# 찾아낸 클로드 패널(TARGET_PANE)로 텍스트를 보내고 엔터(C-m) 입력
tmux send-keys -l -t "$TARGET_PANE" "$PROMPT"
tmux send-keys -t "$TARGET_PANE" C-m
