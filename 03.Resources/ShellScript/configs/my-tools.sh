#!/bin/bash

# ---------------------------------------------------------
# [1] 나만의 명령어 리스트 정의 (형식: "화면에 보일 이름 | 실제 실행할 명령어")
# ---------------------------------------------------------
COMMANDS="
🤖 Claude Code: 현재 프로젝트 분석 | claude -p '이 프로젝트의 구조와 핵심 로직을 분석해서 요약해줘'
🛠️  Tmux 설정 새로고침 | tmux source-file ~/.tmux.conf
🌐 로컬 서버 시작 (예시) | npm run dev
🧹 Docker 사용하지 않는 자원 정리 | docker system prune -af
📂 NvChad 설정 폴더로 이동 | cd ~/.config/nvim && nvim .
"

# ---------------------------------------------------------
# [2] fzf를 사용해 목록을 띄우고 선택받기
# ---------------------------------------------------------
# 빈 줄을 제거하고 fzf를 실행합니다.
SELECTED=$(echo "$COMMANDS" | grep -v '^\s*$' | fzf \
  --prompt="🚀 실행할 작업을 선택하세요 > " \
  --height=40% --layout=reverse --border=rounded)

# 선택을 취소(ESC)한 경우 스크립트 종료
if [ -z "$SELECTED" ]; then
    exit 0
fi

# ---------------------------------------------------------
# [3] 선택한 항목에서 '|' 뒤의 실제 명령어를 추출하여 실행
# ---------------------------------------------------------
CMD=$(echo "$SELECTED" | awk -F ' \\| ' '{print $2}')

# 명령어 실행 전 화면에 출력 (선택 사항)
echo -e "\033[1;32m실행 중:\033[0m $CMD"

# 실제 명령어 실행 (현재 셸의 히스토리에 남기기 위해 eval 사용)
eval "$CMD"
